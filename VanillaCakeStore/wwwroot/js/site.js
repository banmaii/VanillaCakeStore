function getAccessTokenFromCookie() {
    var cookies = document.cookie.split("; ");
    for (var i = 0; i < cookies.length; i++) {
        var cookie = cookies[i].split("=");
        if (cookie[0] === "AccessToken") {
            return cookie[1];
        }
    }
    return "";
}